void domain_expension()
{
    string s;
    cin >> s;
    ll mx = 0;
    ll cnt = 0;
    char ch = s[0];
    for(int i=0;i<s.size();i++){
        if(s[i]==ch){
            cnt++;
        }
        else{
            mx=max(cnt,mx);
            ch = s[i];
            cnt = 1;
        }
    }
    mx = max(mx,cnt);

    cout << mx << endl;
}
